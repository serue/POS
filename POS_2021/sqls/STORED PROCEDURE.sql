BEGIN
	UPDATE USERS SET NAME=@NAME,ID_NO=@ID_NO,ADDRESS=@ADDRESS,GENDER=@GENDER,AGE=@AGE,CONTACT=@CONTACT,USERNAME=@USERNAME,PASSWORD=@PASSWORD WHERE ID=@ID
